PUBLIC PRINT_UD_NUM
PUBLIC PRINT_SH_NUM

EXTRN NUMBER : WORD

DSEG SEGMENT PARA PUBLIC 'DATA'
	MASK2 DW 1
	UD_NUM DW 0
DSEG ENDS

CSEG SEGMENT PARA PUBLIC 'CODE'
	ASSUME DS:DSEG, CS:CSEG

PRINT_UD_NUM PROC NEAR
	
	XOR AX, AX
	MOV AX, 1 ;Счетчик степени
	MOV BX, 16
	
	CONVERT:
		MOV DX, NUMBER
		AND DX, MASK2
		JZ STEP
		ADD UD_NUM, AX
		
		STEP:
		MOV DX, 2
		MUL DX
		DEC BX
		SAR NUMBER, 1
		JNZ CONVERT
		
	XOR AX, AX
	MOV AH, 09h
	MOV DX, 13
	INT 21h
	MOV DX, 10
	INT 21h
	MOV DX, UD_NUM
	SUB DX, '0'
	INT 21h
	
	RET
PRINT_UD_NUM ENDP

