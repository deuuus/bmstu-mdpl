SSEG SEGMENT PARA STACK 'STACK'
	DB 100 DUP (0)
SSEG ENDS

DSEG SEGMENT PARA 'DATA'
	ROW_MSG DB 'ENTER NUM OF ROWS: $'
	COL_MSG DB 'ENTER NUM OF COLS: $'
	MTRX_MSG DB 'ENTER MATRIX ELEMS: $'
	RES_MSG DB 'RESULT: $'
	ROWS DB 0
	COLS DB 0
	DECR DW 0
	MAX_ODD DW 0
	ROW_TO_DEL DW 0
	MATRIX DB 9 * 9 DUP (0)

DSEG ENDS

CSEG SEGMENT PARA 'CODE'
	ASSUME CS:CSEG, DS:DSEG, SS:SSEG
	
INPUT_SYMB:
	MOV AH, 1h
	INT 21h
	RET
	
OUTPUT_SYMB:
	MOV AH, 2h
	INT 21h
	RET
	
CRLF:
	MOV AH, 2h
	MOV DL, 13
	INT 21h
	MOV DL, 10
	INT 21h
	RET
	
PRINT_SPACE:
	MOV AH, 2h
	MOV DL, 32
	INT 21h
	RET
	
MAIN:
	MOV AX, DSEG
	MOV DS, AX
	
	MOV AH, 9h
	MOV DX, OFFSET ROW_MSG
	INT 21H
	CALL CRLF
	
	CALL INPUT_SYMB
	MOV ROWS, AL
	SUB ROWS, '0'
	CALL CRLF
	
	MOV AH, 9h
	MOV DX, OFFSET COL_MSG
	INT 21H
	CALL CRLF
	
	CALL INPUT_SYMB
	MOV COLS, AL
	SUB COLS, '0'
	CALL CRLF
	
	MOV AH, 9h
	MOV DX, OFFSET MTRX_MSG
	INT 21H
	CALL CRLF

	MOV BX, 0
	MOV CL, ROWS
	INPUT_MTRX:
		MOV CL, COLS
		INPUT_ROW:
			CALL INPUT_SYMB
			MOV MATRIX[BX], AL
			SUB MATRIX[BX], '0'
			INC BX
			CALL PRINT_SPACE
			LOOP INPUT_ROW
		CALL CRLF
		MOV CL, ROWS
		MOV SI, DECR
		SUB CX, SI
		INC DECR
		LOOP INPUT_MTRX
	
	MOV DECR, 0
	MOV BX, 0
	MOV CL, ROWS
	FIND_ROW:
		MOV DI, 0
		MOV CL, COLS
		CHECK_COL:
			TEST MATRIX[BX], 1
			JP EVN
			INC DI
			EVN:
			INC BX
			LOOP CHECK_COL
		CMP DI, MAX_ODD
		JNAE NOT_AE
		MOV MAX_ODD, DI
		MOV DI, DECR
		MOV ROW_TO_DEL, DI
		NOT_AE:
		MOV CL, ROWS
		MOV SI, DECR
		SUB CX, SI
		INC DECR
		LOOP FIND_ROW
		
	MOV BL, COLS
	MOV AX, 1
	MUL BX
	MUL ROW_TO_DEL
	MOV BX, AX
	
	MOV DI, ROW_TO_DEL
	MOV DECR, DI
	
	MOV CL, ROWS
	SUB CX, ROW_TO_DEL
	
	DEL:
		MOV CL, COLS
		DEL_COL:
			ADD BL, COLS
			MOV AH, MATRIX[BX]
			SUB BL, COLS
			MOV MATRIX[BX], AH
			INC BX
			LOOP DEL_COL
		MOV CL, ROWS
		MOV SI, DECR
		SUB CX, SI
		INC DECR
		LOOP DEL
		
	SUB ROWS, 1
	
	MOV AH, 9h
	MOV DX, OFFSET RES_MSG
	INT 21H
	CALL CRLF
	
	MOV DECR, 0
	MOV BX, 0
	MOV CL, ROWS
	OUTPUT_MTRX:
		MOV CL, COLS
		OUTPUT_ROW:
			MOV DL, MATRIX[BX]
			ADD DL, '0'
			CALL OUTPUT_SYMB
			INC BX
			CALL PRINT_SPACE
			LOOP OUTPUT_ROW
		CALL CRLF
		MOV CL, ROWS
		MOV SI, DECR
		SUB CX, SI
		INC DECR
		LOOP OUTPUT_MTRX
		
	MOV AX, 4C00h
	INT 21h

CSEG ENDS
END MAIN
	